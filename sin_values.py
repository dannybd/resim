# Different systems disagree on the value of sin(x) in the lowest bits;
# this causes a lot of thrash. To streamline this, we hardcode the NodeJS
# output values of our sin(x) computations, which themselves take a finite
# set of values thank to their inputs.

# buoyancy ranges [0, 2], so round(10 * buoyancy) ranges [0, 20]
# frequency ranges [6, 26]
# day ranges [0, 239] a bit higher than max ever observed

# NodeJS implementation:
"""
function __test_nodejs_values() {
    const FREQ_RANGE = [6, 26];
    const NUM_DAYS = 240;
    const values = {};
    for (let frequency = FREQ_RANGE[0]; frequency <= FREQ_RANGE[1]; frequency++) {
        values[frequency] = [...Array(NUM_DAYS).keys()].map(
            day => Math.sin(Math.PI * ((2 / frequency) * day + 0.5)),
        );
    }
    return values;
}
"""

